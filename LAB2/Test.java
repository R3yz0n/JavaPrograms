// Q.N-1 Java program to illustrate the application of literals
public class Test {
    public static void main(String[] args){
        int a=101; //integer-form literal


        float b= (float) 101.230; //decimal-form literal

        char ch='a'; //single character literal with single quote
        String s="Hello"; //String literals
        boolean d=false; //boolean literals

        System.out.println(a);
        System.out.println(b);
        System.out.println(ch);
        System.out.println(s);
        System.out.println(d);
    }

}